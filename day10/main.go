var pairs = map[byte]byte{
	'(': ')',
	'{': '}',
	'[': ']',
	'<': '>',
}

func part1() {

	var scores = map[byte]int{
		')': 3,
		']': 57,
		'}': 1197,
		'>': 25137,
	}
	res := 0
	lines := strings.Split(input, "\n")
	for _, line := range lines {
		var errch byte
		var stack []byte
		for _, c := range []byte(line) {
			c2, ok := pairs[c]
			if ok {
				stack = append(stack, c2)
			} else if len(stack) > 0 && stack[len(stack)-1] == c {
				stack = stack[:len(stack)-1]
			} else {
				errch = c
				break
			}
		}
		if errch != 0 {
			res += scores[errch]
		}
	}
	fmt.Println(res)
}

func part2() {

	var scores = map[byte]int{
		')': 1,
		']': 2,
		'}': 3,
		'>': 4,
	}
	allscores := []int{}
	lines := strings.Split(input, "\n")
	for _, line := range lines {
		var errch byte
		var stack []byte
		for _, c := range []byte(line) {
			c2, ok := pairs[c]
			if ok {
				stack = append(stack, c2)
			} else if len(stack) > 0 && stack[len(stack)-1] == c {
				stack = stack[:len(stack)-1]
			} else {
				errch = c
				break
			}
		}
		if errch != 0 {
			continue
		}
		score := 0
		for i := len(stack) - 1; i >= 0; i-- {
			score = score*5 + scores[stack[i]]
		}
		allscores = append(allscores, score)
	}
	sort.Ints(allscores)
	fmt.Println(allscores[len(allscores)/2])
}

